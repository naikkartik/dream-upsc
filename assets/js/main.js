/* ═══════════════════════════════════════════════
   STATS
   ═══════════════════════════════════════════════ */
function updateStats() {
  const gapCount = [...NOTES_DATA, ...PRELIMS_DATA, ...MAINS_DATA]
    .filter(d => d.gapStatus === 'partial').length;

  animateCount(totalNotes,   NOTES_DATA.length);
  animateCount(totalPrelims, PRELIMS_DATA.length);
  animateCount(totalMains,   MAINS_DATA.length);
  animateCount(totalGaps,    gapCount);
}

function animateCount(el, target) {
  if (!el) return;
  const start = 0;
  const duration = 600;
  const startTime = performance.now();
  const update = (now) => {
    const elapsed = now - startTime;
    const progress = Math.min(elapsed / duration, 1);
    el.textContent = Math.round(start + (target - start) * easeOut(progress));
    if (progress < 1) requestAnimationFrame(update);
  };
  requestAnimationFrame(update);
}
const easeOut = t => 1 - Math.pow(1 - t, 3);


/* ═══════════════════════════════════════════════
   RENDER
   ═══════════════════════════════════════════════ */
function renderAll() {
  renderSection(NOTES_DATA,   notesGrid,   notesCount,   'notesEmpty',   '📄');
  renderSection(PRELIMS_DATA, prelimsGrid, prelimsCount, 'prelimsEmpty', '🎯');
  renderSection(MAINS_DATA,   mainsGrid,   mainsCount,   'mainsEmpty',   '✍️');
}

function renderSection(data, grid, countEl, emptyId, icon) {
  const filtered = applyFilters(data);
  const emptyEl  = document.getElementById(emptyId);

  // remove old cards (keep empty state)
  Array.from(grid.children).forEach(c => {
    if (!c.classList.contains('empty-state')) c.remove();
  });

  if (filtered.length === 0) {
    if (emptyEl) emptyEl.classList.remove('hidden');
    if (countEl) countEl.textContent = '0';
    return;
  }

  if (emptyEl) emptyEl.classList.add('hidden');
  if (countEl) countEl.textContent = filtered.length;

  filtered.forEach(item => {
    const card = buildCard(item, icon);
    grid.insertBefore(card, emptyEl);
  });
}

function applyFilters(data) {
  return data.filter(item => {
    const matchFilter = state.activeFilter === 'all' || item.gs === state.activeFilter;
    const q = state.searchQuery.toLowerCase();
    const matchSearch = !q
      || item.title.toLowerCase().includes(q)
      || item.subtopic.toLowerCase().includes(q)
      || (item.summary && item.summary.toLowerCase().includes(q));
    return matchFilter && matchSearch;
  });
}

function buildCard(item, icon) {
  const card = document.createElement('a');
  card.className = `topic-card ${item.gs}`;
  card.href = item.file || '#';
  if (!item.file || item.file === '#') {
    card.setAttribute('href', '#');
    card.addEventListener('click', (e) => { e.preventDefault(); openModal(item); });
  }

  const gapChip = item.gapStatus === 'partial'
    ? `<span class="card-chip gap-chip">⚠ partial</span>`
    : `<span class="card-chip covered-chip">✅ complete</span>`;

  const pyqChips = (item.pyqYears || [])
    .map(y => `<span class="card-chip">${y}</span>`).join('');

  card.innerHTML = `
    <div class="card-top">
      <span class="card-badge badge-${item.gs}">${item.gs.toUpperCase()}</span>
      <span class="card-type-icon">${icon}</span>
    </div>
    <div>
      <div class="card-title">${item.title}</div>
      <div class="card-subtopic">${item.subtopic}</div>
    </div>
    ${item.summary ? `<p style="font-size:12px;color:var(--text-mute);line-height:1.5">${item.summary}</p>` : ''}
    <div class="card-meta">
      ${gapChip}
      ${pyqChips}
    </div>
  `;
  return card;
}


/* ═══════════════════════════════════════════════
   MODAL
   ═══════════════════════════════════════════════ */
function openModal(item) {
  modalBody.innerHTML = `
    <div class="rev-card-header">
      <div class="rev-card-gs">${item.gs.toUpperCase()} · ${item.type}</div>
      <div class="rev-card-title">${item.title}</div>
      <div class="rev-card-syllabus">${item.subtopic}</div>
    </div>
    ${item.summary ? `<p style="font-size:13px;color:var(--text-sec);margin-bottom:16px">${item.summary}</p>` : ''}
    <div class="rev-section">
      <div class="rev-section-title">PYQ Years</div>
      <div>${(item.pyqYears || []).map(y => `<span class="rev-pyq-chip">${y}</span>`).join('') || '<span style="color:var(--text-mute);font-size:12px">None on record</span>'}</div>
    </div>
    <div class="rev-section">
      <div class="rev-section-title">Gap Status</div>
      <p style="font-size:13px;color:${item.gapStatus === 'partial' ? 'var(--red)' : 'var(--green)'}">${item.gapStatus === 'partial' ? '⚠ Partial coverage — some PYQs not fully addressed' : '✅ Complete — all PYQs covered'}</p>
    </div>
    <div class="rev-footer">
      <span>Source: ${item.source || '—'}</span>
      <span>Created: ${item.createdAt || '—'}</span>
      <span>ID: ${item.id}</span>
    </div>
  `;
  modalOverlay.setAttribute('aria-hidden', 'false');
  modalOverlay.classList.add('open');
  document.body.style.overflow = 'hidden';
}

function closeModal() {
  modalOverlay.classList.remove('open');
  modalOverlay.setAttribute('aria-hidden', 'true');
  document.body.style.overflow = '';
}


/* ═══════════════════════════════════════════════
   EVENTS
   ═══════════════════════════════════════════════ */
function bindEvents() {

  /* Search */
  searchInput.addEventListener('input', () => {
    state.searchQuery = searchInput.value.trim();
    renderAll();
  });
  document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') {
      if (modalOverlay.classList.contains('open')) {
        closeModal();
      } else {
        searchInput.value = '';
        state.searchQuery = '';
        renderAll();
        searchInput.blur();
      }
    }
    if (e.key === '/' && document.activeElement !== searchInput) {
      e.preventDefault();
      searchInput.focus();
    }
  });

  /* Filters */
  filterBtns.forEach(btn => {
    btn.addEventListener('click', () => {
      filterBtns.forEach(b => b.classList.remove('active'));
      btn.classList.add('active');
      state.activeFilter = btn.dataset.filter;
      renderAll();
    });
  });

  /* Modal */
  modalClose.addEventListener('click', closeModal);
  modalOverlay.addEventListener('click', (e) => {
    if (e.target === modalOverlay) closeModal();
  });

  /* Nav active state on scroll */
  const sections = document.querySelectorAll('section[id]');
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        navLinks.forEach(l => l.classList.remove('active'));
        const activeLink = document.querySelector(`.nav-link[href="#${entry.target.id}"]`);
        if (activeLink) activeLink.classList.add('active');
      }
    });
  }, { threshold: 0.4 });
  sections.forEach(s => observer.observe(s));
}


